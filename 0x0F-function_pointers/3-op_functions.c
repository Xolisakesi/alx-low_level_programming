#include "3-calc.h"
/**
 * op_add - returns the sum of a and b.
 * @a: first parameter
 * @b: second parameter
 * Return: sum of a nad b
 */
int op_add(int a, int b)
{
return (a + b);
}
/**
 * op_sub - returns the difference of a and b.
 * @a: first parameter
 * @b: second parameter
 * Return: difference ofa and b
 */
int op_sub(int a, int b)
{
return (a - b);
}
/**
 * op_mul - returns the product of a and b.
 * @a: first parameter
 * @b: second parameter
 * Return: returns the product of a and b
 */
int op_mul(int a, int b)
{
return (a * b);
}
/**
 * op_div - returns the result of the division of
 * @a: first parameter
 * @b: second parameter
 * Return: results of divition
 */
int op_div(int a, int b)
{
return (a / b);
}
/**
 * op_mod - returns the remainder of the division
 * @a: first parameter
 * @b: second parameter
 * Return: remainder of divition
 */
int op_mod(int a, int b)
{
return (a % b);
}
